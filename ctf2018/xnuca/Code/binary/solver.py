from z3 import *
x=BitVec('x',32)
y=BitVec('y',32)
z=BitVec('z',32)
s=Solver()
s.add(x&0xff==0x5E)
s.add(z&0xff==0x5E)
s.add(y&0x00ff0000==0x5E0000)
s.add((x>>4)*0x15-z==0x1D7ECC6B)
s.add((x>>8)+y==0x5FBCBDBD)
s.add((z>>8)*0x03+y==0x6079797C)
print(s.check())
m=s.model()
X=m[x].as_long()
Z=m[z].as_long()
Y=m[y].as_long()
print('X-NUCA{%x%x%x}'%(X,Y,Z))